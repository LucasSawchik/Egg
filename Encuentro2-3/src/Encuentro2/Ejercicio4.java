
package Encuentro2;
import java.util.Scanner;

public class Ejercicio4 {

    
    public static void main(String[] args) {
        System.out.println("Ingrese la cantidad de °C a convertir a °F");
        Scanner t = new  Scanner(System.in);
        double Cent = t.nextDouble();
        double Far =  32 + (9 * Cent / 5);
        
        System.out.println("El resultado es: " + Far + "°F");
        
        
        
        
        
        
        
        
        
    }
    
}
